int heigh(Node* node, int* balance){
    if(*balance==0)
    return 0;
    if(node==NULL)
    return 0;
    int lef = 1 + heigh(node->left,balance);
    int righ = 1 + heigh(node->right,balance);
    if(abs(lef-righ)>1)
    *balance=0;
    return max(lef,righ);
}

